package com.java.variable;

public class Initialized {

    public static void main(String[] args) {

        // default initialized

        byte a = 0;
        short b = 0;
        int c = 0;
        long d = 0;
        float e = 0.0f;
        double f = 0.0d;
        char g = '\u0000';
        String h = null; // or can be in any objects
        boolean i = false;

        // make sure that the local variable is always initialized.
        // 'Cause, Accessing an uninitialized local variable will result in a compile-time error.

    }

}
